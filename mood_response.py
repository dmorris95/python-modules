
def mood_management(mood):
    if mood.lower() == "happy":
        return "Hope your happy mood continues!"
    elif mood.lower() == "sad":
        return "Hope you are able to brighten your mood soon!"
    elif mood.lower() == "excited":
        return "I hope your day continues to be exciting!"
    elif mood.lower() == "mad":
        return  "You should not stay angry for too long."